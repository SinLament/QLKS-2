    //Load tìm kiếm thông tin lên bảng
    public void loadTT() {
        String maNV = txtTimMaNV.getText();
        NhanVien nv = nhanVienDao.findByMaNV(maNV);
        if(nv != null) {
            DefaultTableModel model = (DefaultTableModel) tblDanhSach.getModel();
            model.setRowCount(0);
            try {           
                String vaiTro;
                if(nv.getVaiTro() == true) {
                    vaiTro = "Quản lý";
                } else {
                    vaiTro = "Nhân viên";
                }
                Object[] row = {
                    nv.getMaNv(),
                    nv.getHoTen(),
                    nv.getMatKhau(),
                    vaiTro
                };
                model.addRow(row);
            } catch (Exception e) {
                e.getMessage();
                DialogHelper.alert(this, "Lỗi truy xuất");
            }
        } else {
            DialogHelper.alert(this, "Không tồn tại");
            load();
        }
        
    }
    
    //Load toàn bộ thông tin lên bảng
    public void load() {
        List<NhanVien> list = nhanVienDao.select();
        DefaultTableModel model = (DefaultTableModel) tblDanhSach.getModel();
        model.setRowCount(0);
        try {
            for(NhanVien nv : list) {
                String vaiTro;
                if(nv.getVaiTro() == true) {
                    vaiTro = "Quản lý";
                } else {
                    vaiTro = "Nhân viên";
                }
                Object[] row = {
                    nv.getMaNv(),
                    nv.getHoTen(),
                    nv.getMatKhau(),
                    vaiTro
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.getMessage();
            DialogHelper.alert(this, "Lỗi truy xuất");
        }
    }
    
