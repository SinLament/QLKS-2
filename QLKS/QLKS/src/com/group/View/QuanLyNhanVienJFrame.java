    //Tạo mã nhân viên
    public String getMaNV() {
        Random random = new Random();
        int min = 100;  
        int max = 999;

        int randomNumber = random.nextInt(max - min + 1) + min;
        String maNv = "NV" + randomNumber;
        
        NhanVien checkDP = nhanVienDao.findByMaNV(maNv);
        if (checkDP != null) {
            randomNumber = random.nextInt(max - min + 1) + min;
            maNv = "NV" + randomNumber;
        }
        return maNv;
    }

    //Thêm nhân viên
    public void insert() {
        if (validateAdd()) {
            String maNV = txtMaNV.getText();
            String hoTen = txtHoVaTen.getText();
            String matKhau = txtMatKhau.getText();
            boolean vaiTro = rdQL.isSelected();

            NhanVien model = new NhanVien(maNV, hoTen, matKhau, vaiTro);
            nhanVienDao.insert(model);
            DialogHelper.alert(rootPane, "Thêm nhân viên thành công");
            clearTT();
            btnThem.setEnabled(false);
        }
    }
    
    //Cập nhật nhân viên
    public void update() {
        if (validateAdd()) {
            String maNV = txtMaNV.getText();
            String hoTen = txtHoVaTen.getText();
            String matKhau = txtMatKhau.getText();
            boolean vaiTro = rdQL.isSelected();

            NhanVien model = new NhanVien(maNV, hoTen, matKhau, vaiTro);
            nhanVienDao.update(model);
            DialogHelper.alert(rootPane, "Cập nhật nhân viên thành công");
            clearTT();
            btnSua.setEnabled(false);
            btnXoa.setEnabled(false);
        }
    }
    
    //Xóa nhân viên
    public void delete() {
        if (txtMaNV.getText().equals(ShareHelper.USER.getMaNv())) {
            DialogHelper.alert(rootPane, "Không được tự xóa tài khoản của mình!");
        } else {
            int choice = JOptionPane.showConfirmDialog(null, "Xác nhận xóa?", "Xóa Nhân viên", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                String maNV = txtMaNV.getText();
                nhanVienDao.delete(maNV);
                DialogHelper.alert(rootPane, "Xóa nhân viên thành công");
                clearTT();
                btnSua.setEnabled(false);
                btnXoa.setEnabled(false);
            }
        }
    }
    
    //Check điền thông tin
    public boolean validateAdd() {
        String hoTen = txtHoVaTen.getText().trim();
        if (hoTen.equals("")) {
            DialogHelper.alert(rootPane, "Vui lòng nhập họ tên");
            return false;
        } 
        
        String matKhau = txtMatKhau.getText().trim();
        if (matKhau.equals("")) {
            DialogHelper.alert(rootPane, "Vui lòng nhập mật khẩu");
            return false;
        } 
        
        if (!(rdNV.isSelected() || rdQL.isSelected())) {
            DialogHelper.alert(rootPane, "Vui lòng chọn chức vụ");
            return false;
        }
        return true;
    }
