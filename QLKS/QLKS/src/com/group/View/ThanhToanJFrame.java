    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        int choice = JOptionPane.showConfirmDialog(null, "Xác nhận thanh toán?", "Thanh toán", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            String sql = "Update Phong set tinhtrang = 0 where sophong = " + soPhong1;
            JDBCHelper.executeUpdate(sql);
            
            DatPhong datphong = datPhongDao.findBySoPhong(soPhong1);
            List<HoaDon> list = hoaDonDao.findByMaDat1(datphong.getMaDatPhong());
            if (list != null) {
                DatPhong datPhong = datPhongDao.findBySoPhong(soPhong1);
                String sqlHoaDon = "Delete from hoadon where madat like '" + datPhong.getMaDatPhong() +"'";
                JDBCHelper.executeUpdate(sqlHoaDon);
            }
            String deleteSQL = "Delete from datphong where sophong = " + soPhong1;
            JDBCHelper.executeUpdate(deleteSQL);
            
            DialogHelper.alert(rootPane, "Thanh toán và trả phòng thành công");
        }
    }//GEN-LAST:event_btnThanhToanActionPerformed

    public void load() {
        double tongTien = 0;
        //Get thông tin
        int id = Integer.parseInt(soPhong1);
        DichVu dichVu = null;
        Phong phong = phongDao.findBySoPhong(id);
        DatPhong datPhong = datPhongDao.findBySoPhong(soPhong1);
        KhachHang khachHang = khachHangDao.findBySDT(datPhong.getSDTKhach());
        
        List<HoaDon> list = hoaDonDao.findByMaDat1(datPhong.getMaDatPhong());
        for (HoaDon hoaDon : list) {
            dichVu = dichVuDao.findByMaDV(hoaDon.getMaDV());
            tongTien += dichVu.getGia();
            System.out.println(tongTien);
            if(dichVu.getMaDV().equals("DV001")) {
                DV001.setSelected(true);
            }
            if(dichVu.getMaDV().equals("DV002")) {
                DV002.setSelected(true);
            }
            if(dichVu.getMaDV().equals("DV003")) {
                DV003.setSelected(true);
            }
            if(dichVu.getMaDV().equals("DV004")) {
                DV004.setSelected(true);
            }
            if(dichVu.getMaDV().equals("DV005")) {
                DV005.setSelected(true);
            }
        }
        
        //Set thông tin phòng
        txtSoPhong.setText(soPhong1);        
        txtLoaiPhong.setText(phong.getLoaiPhong());
        
        //Set Thông tin khách hàng
        txtTenKH.setText(khachHang.getHoTen());
        
        //Set Thông tin dịch vụ
        
        
        //Set Thời gian
        txtNgayTra.setText(DateHelper.toString(datPhong.getNgayDi(), "dd/MM/yyyy"));
        String soNgay = null;
        long tg = 0;
        try {
            tg = getSoNgayThue(datPhong.getNgayDen(), datPhong.getNgayDi());
            soNgay = String.valueOf(tg);
        } catch (ParseException e) {
            e.getMessage();
        }
        txtTongTG.setText(soNgay);
        
        //Tính tiền và Set tiền
        tongTien += (phong.getGia() * tg);
        
        txtTongThanhToan.setText(formatTienVND(tongTien));
    }
    
