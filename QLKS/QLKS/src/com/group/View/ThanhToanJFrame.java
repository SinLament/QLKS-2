    //Tính số ngày
    private long getSoNgayThue(Date ngayDen, Date ngayDi) throws ParseException {
        long diffDays = 0;
        try {
        String ngayDen1 = DateHelper.toString(ngayDen, "dd/MM/yyyy");
        String ngayDi1 = DateHelper.toString(ngayDi, "dd/MM/yyyy");
        
        Date date1 = new SimpleDateFormat("dd/MM/yyyy").parse(ngayDen1);
        Date date2 = new SimpleDateFormat("dd/MM/yyyy").parse(ngayDi1);

        long diff = date2.getTime() - date1.getTime();
        diffDays = diff / (24 * 60 * 60 * 1000);
        } catch (ExceptionInInitializerError e) {
            e.getMessage();
        }
        return diffDays;
    }
    
    //Định dạng tiền
    private static String formatTienVND(double giaTien) {
        DecimalFormat decimalFormat = new DecimalFormat("#,##0 VND");
        return decimalFormat.format(giaTien);
    }
