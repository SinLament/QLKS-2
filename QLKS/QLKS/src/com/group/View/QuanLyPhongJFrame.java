    public void insert() {
        //Thu thông tin
        String maDatPhong = txtMaDatPhong.getText();
        String maNV = ShareHelper.USER.getMaNv();
        
        
        String hoTen = txtHoVaTen.getText();
        String soCMND = txtCCCD.getText();
        String soPhong = txtSoPhong.getText();

        int SDTKhach = Integer.parseInt(txtSDT.getText());

        Date ngayDen = DateHelper.toDate(txtNgayDen.getText(), "dd/MM/yyyy");
        Date ngayDi = DateHelper.toDate(txtNgayDi.getText(), "dd/MM/yyyy");

        //Kiểm tra khách tồn tại
        KhachHang checkKH = khachHangDao.findBySDT(SDTKhach);

        //Tạo đối tượng & đưa vào database
        if(checkKH == null) {
            KhachHang khach = new KhachHang(SDTKhach, hoTen, soCMND);
            khachHangDao.insert(khach);
        }
        
        //Tạp đặt phòng
        DatPhong model = new DatPhong(maDatPhong, maNV, SDTKhach, ngayDen, ngayDi, soPhong);
        datPhongDao.insert(model);

        //Tạo hóa đơn
        if(DV001.isSelected()) {
            HoaDon hd = new HoaDon(SDTKhach, maDatPhong, "DV001");
            hoaDonDao.insert(hd);
        }
        if(DV002.isSelected()) {
            HoaDon hd = new HoaDon(SDTKhach, maDatPhong, "DV002");
            hoaDonDao.insert(hd);
        }
        if(DV003.isSelected()) {
            HoaDon hd = new HoaDon(SDTKhach, maDatPhong, "DV003");
            hoaDonDao.insert(hd);
        }
        if(DV004.isSelected()) {
            HoaDon hd = new HoaDon(SDTKhach, maDatPhong, "DV004");
            hoaDonDao.insert(hd);
        }
        if(DV005.isSelected()) {
            HoaDon hd = new HoaDon(SDTKhach, maDatPhong, "DV005");
            hoaDonDao.insert(hd);
        }
    
        
        String sql = "Update Phong set tinhtrang = 1 where sophong = " + soPhong;
        JDBCHelper.executeUpdate(sql);
        
        DialogHelper.alert(rootPane, "Đặt phòng thành công");  
        clearDP();
    }
    
    //Tính số ngày
    private long getSoNgayThue(Date ngayDen, Date ngayDi) throws ParseException {
        long diffDays = 0;
        try {
        String ngayDen1 = DateHelper.toString(ngayDen, "dd/MM/yyyy");
        String ngayDi1 = DateHelper.toString(ngayDi, "dd/MM/yyyy");
        
        Date date1 = new SimpleDateFormat("dd/MM/yyyy").parse(ngayDen1);
        Date date2 = new SimpleDateFormat("dd/MM/yyyy").parse(ngayDi1);

        long diff = date2.getTime() - date1.getTime();
        diffDays = diff / (24 * 60 * 60 * 1000);
        } catch (ExceptionInInitializerError e) {
            e.getMessage();
        }
        return diffDays;
    }
    
