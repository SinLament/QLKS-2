    public String getMaDP() {
        Random random = new Random();
        int min = 1000;  
        int max = 9999;

        int randomNumber = random.nextInt(max - min + 1) + min;
        String maDat = "DP" + randomNumber;
        
        DatPhong checkDP = datPhongDao.findByMaDat(maDat);
        if (checkDP != null) {
            randomNumber = random.nextInt(max - min + 1) + min;
            maDat = "DP" + randomNumber;
        }
        return maDat;
    }
    
