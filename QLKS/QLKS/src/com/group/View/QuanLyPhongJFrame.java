    public boolean validateAdd() {
        String hoTen = txtHoVaTen.getText().trim();
        String soCMND = txtCCCD.getText().trim();
        
        String SDTKhach = txtSDT.getText();
        
        try {
            int SDTKhach1 = Integer.parseInt(txtSDT.getText());
        } catch (NumberFormatException e) {
            DialogHelper.alert(rootPane, "Số điện thoại không đúng định dạng");
            return false;
        }
        
        String ngayDen = txtNgayDen.getText();
        String ngayDi = txtNgayDi.getText();
        
        if (hoTen.equals("")) {
            DialogHelper.alert(rootPane, "Vui lòng nhập họ tên");
            return false;
        } 
        
        if (soCMND.equals("")) {
            DialogHelper.alert(rootPane, "Vui lòng nhập họ tên");
            return false;
        } 
        
        if (SDTKhach.equals("")) {
            DialogHelper.alert(rootPane, "Vui lòng nhập số điện thoại");
            return false;
        }
        
        String dateFormat = "dd/MM/yyyy";

        try {
            SimpleDateFormat sdf = new SimpleDateFormat(dateFormat);
            sdf.parse(ngayDen);
            sdf.parse(ngayDi);
        } catch (ParseException e) {
            DialogHelper.alert(rootPane, "Ngày đến và đi sai định dạng 'dd/MM/yyyy'");
            return false;
        }
        return true;
    }
    
