    private void tblDanhsachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhsachMouseClicked
        this.index = tblDanhsach.rowAtPoint(evt.getPoint());
        
        if (mode == 1) {
            if (this.index >= 0) {
                String id = (String) tblDanhsach.getValueAt(this.index, 0);
                txtSoPhong.setText(id);
                txtMaDatPhong.setText(getMaDP());
                txtNgayDen.setText(DateHelper.toString(DateHelper.now(), "dd/MM/yyyy"));
                jTabbedPane1.setSelectedIndex(1);
            }
        } else {
            if (this.index >= 0) {
                String id = (String) tblDanhsach.getValueAt(this.index, 0);
                int soPhong = Integer.parseInt(id);
                Phong phong = phongDao.findBySoPhong(soPhong);
                DatPhong datphong = datPhongDao.findBySoPhong(id);
                txtSoPhong1.setText(id);
                txtLoaiPhong.setText(phong.getLoaiPhong());
                txtNgayThue1.setText(DateHelper.toString(datphong.getNgayDen(),"dd/MM/yyyy"));
                txtNgayTra.setText(DateHelper.toString(datphong.getNgayDi(),"dd/MM/yyyy"));
                txtMaDat.setText(datphong.getMaDatPhong());
                
                long ngayThue = 0;
                try {
                    ngayThue = getSoNgayThue(datphong.getNgayDen(), datphong.getNgayDi());
                } catch (ParseException ex) {
                    Logger.getLogger(QuanLyPhongJFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                String ngay = String.valueOf(ngayThue);
                txtTongGio.setText(ngay);
                jTabbedPane1.setSelectedIndex(2);
            }
        }
    }//GEN-LAST:event_tblDanhsachMouseClicked

