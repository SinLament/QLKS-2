    public void load() {
        DefaultTableModel model = (DefaultTableModel) tblDanhsach.getModel();
        model.setRowCount(0);
        try {
            for(Phong phong : list) {
                Object[] row = {
                    phong.getSoPhong(),
                    phong.getLoaiPhong(),
                    phong.getGia(),
                    phong.getTinhTrang()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.getMessage();
            DialogHelper.alert(this, "Lỗi truy xuất");
        }
    }
    
